# Aula 3 - Tipo int e float (numeros) - introdução aos tipos de dados

'''
Tipo int
int -> Número inteiro
O tipo int representa qualquer numero positivo ou negativo 
int sem sinal é considerado positivo

'''
print(11)   # int
print(11)   # int

'''
float -> Número com ponto flutuante
O tipo float representa qualquer número positivo ou negativo com um ponto flutuante
float sem sinal é considerado positivo

'''
print(1.1, -1.1) # Usa-se ponto para separar as casas decimais 

# A função type mostra o tipo que o Phyton inferiu o valor
print(type('Flavio'))
print(type(0))
print(type(1.1))
print(type(-1.1),type(1.0))